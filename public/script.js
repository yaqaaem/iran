function aggregateCountries(items) {
  const map = {};
  items.forEach((item) => {
    if (!map[item.country]) map[item.country] = { count: 0, amount: 0 };
    map[item.country].count += 1;
    map[item.country].amount += item.amount;
  });
  return Object.entries(map).map(([country, v]) => ({ country, ...v })).sort((a, b) => b.amount - a.amount);
}

