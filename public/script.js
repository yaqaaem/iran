function aggregateCountries(reports) {
  const map = {};
  for (const item of reports) {
    if (!map[item.country]) {
      map[item.country] = { count: 0, amount: 0 };
    }
    map[item.country].count += 1;
    map[item.country].amount += item.amount;
  }
  return Object.entries(map)
    .map(([country, data]) => ({ country, ...data }))
    .sort((a, b) => b.amount - a.amount);
}

function renderCountryTable(countryStats) {
  const tbody = $("countryTableBody");
  tbody.innerHTML = "";

  countryStats.forEach((row) => {
    const meta = countryMeta[row.country] || { flag: "🌍" };
    const tr = document.createElement("tr");
    tr.innerHTML = `
      <td>${meta.flag} ${row.country}</td>
      <td>${row.count}</td>
      <td>${row.amount} USD</td>
    `;
    tbody.appendChild(tr);
  });
}

function renderMapMarkers(countryStats) {
  const holder = $("mapMarkers");
  holder.innerHTML = "";

  countryStats.forEach((row) => {
    const meta = countryMeta[row.country];
    if (!meta) return;

    const g = document.createElementNS("http://www.w3.org/2000/svg", "g");
    g.setAttribute("class", "map-marker");

    const circle = document.createElementNS("http://www.w3.org/2000/svg", "circle");
    circle.setAttribute("cx", meta.x);
    circle.setAttribute("cy", meta.y);
    circle.setAttribute("r", 10);

    const text = document.createElementNS("http://www.w3.org/2000/svg", "text");
    text.setAttribute("x", meta.x + 15);
    text.setAttribute("y", meta.y + 4);
    text.textContent = `${meta.flag} ${row.count}`;

    g.appendChild(circle);
    g.appendChild(text);
    holder.appendChild(g);
  });
}

function renderRecentReports() {
  const holder = $("recentReportsList");
  holder.innerHTML = "";
  const t = i18n[currentLang];

  sampleReports.slice(0, 20).forEach((item) => {
    const meta = countryMeta[item.country] || { flag: "🌍" };
    const card = document.createElement("article");
    card.className = "report-item";
    card.innerHTML = `
      <div class="report-item-top">
        <div>
          <div class="report-name">${item.name}</div>
          <div class="report-country">${meta.flag} <span>${t.reportCountry}: ${item.country}</span></div>
        </div>
        <div class="report-country">${t.reportTime}: ${item.time}</div>
      </div>

      <div class="report-tags">
        <span class="report-tag">${t.reportAmount}: ${item.amount} USD</span>
        <span class="report-tag">${t.reportAgent}: ${officeName(item.agent)}</span>
      </div>

      <div class="report-note">${item.note || t.reportNoteFallback}</div>
    `;
    holder.appendChild(card);
  });
}

function renderStats(countryStats) {
  $("statTotalReports").textContent = sampleReports.length;
  $("statCountries").textContent = countryStats.length;
  const totalAmount = sampleReports.reduce((sum, item) => sum + item.amount, 0);
  $("statTotalAmount").textContent = `${totalAmount} USD`;
  $("sampleReportsCount").textContent = sampleReports.length;
}

function renderReports() {
  const countryStats = aggregateCountries(sampleReports);
  renderCountryTable(countryStats);
  renderMapMarkers(countryStats);
  renderRecentReports();
  renderStats(countryStats);
}

