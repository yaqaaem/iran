let currentLang = "fa";
let selectedAmount = 10;
let selectedAgent = 1;

const $ = (id) => document.getElementById(id);

function applyLanguage(lang) {
  currentLang = lang;
  const t = content[lang];

  document.documentElement.lang = t.lang;
  document.documentElement.dir = t.dir;
  document.body.dir = t.dir;

  $("title").textContent = t.title;
  $("subtitle").textContent = t.subtitle;
  $("amountTitle").textContent = t.amountTitle;
  $("currencyLabel").textContent = t.currencyLabel;
  $("customAmountLabel").textContent = t.customAmountLabel;
  $("customAmount").placeholder = t.customAmountPlaceholder;
  $("amountNotice").textContent = t.amountNotice;
  $("agentTitle").textContent = t.agentTitle;
  $("agent1Badge").textContent = t.agent1Badge;
  $("agent2Badge").textContent = t.agent2Badge;
  $("agent1Name").textContent = content[lang].agent1Name;
  $("agent2Name").textContent = content[lang].agent2Name;
  $("agent1Desc").textContent = t.agent1Desc;
  $("agent2Desc").textContent = t.agent2Desc;
  $("payInfoTitle").textContent = t.payInfoTitle;
  $("selectedAmountTextLabel").textContent = t.selectedAmountTextLabel;
  $("selectedAgentTextLabel").textContent = t.selectedAgentTextLabel;
  $("masterLabel").textContent = t.masterLabel;
  $("zainLabel").textContent = t.zainLabel;
  $("phoneLabel").textContent = t.phoneLabel;
  $("qrLabel").textContent = t.qrLabel;
  $("copyMasterBtn").textContent = t.copy;
  $("copyZainBtn").textContent = t.copy;
  $("copyPhoneBtn").textContent = t.copy;
  $("copyQrBtn").textContent = t.copy;
  $("confirmBtn").textContent = t.confirmBtn;
  $("contactBtn").textContent = t.contactBtn;
  $("helperText").textContent = t.helperText;
  $("reportTitle").textContent = t.reportTitle;
  $("payerNameLabel").textContent = t.payerNameLabel;
  $("payerName").placeholder = t.payerNamePlaceholder;
  $("payerContactLabel").textContent = t.payerContactLabel;
  $("payerContact").placeholder = t.payerContactPlaceholder;
  $("paymentNoteLabel").textContent = t.paymentNoteLabel;
  $("paymentNote").placeholder = t.paymentNotePlaceholder;
  $("submitReportBtn").textContent = t.submitReportBtn;
  $("successTitle").textContent = t.successTitle;
  $("successText").textContent = t.successText;

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });

  updateSummary();
  updateAgentInfo();
}

function getAmountValue() {
  const custom = Number($("customAmount").value);
  if (custom > 0) return custom;
  return selectedAmount;
}

function updateSummary() {
  const amount = getAmountValue();
  $("selectedAmountText").textContent = `${amount} USD`;
  $("selectedAgentText").textContent = agents[selectedAgent].name[currentLang];
}

function updateAgentInfo() {
  const agent = agents[selectedAgent];
  $("masterValue").textContent = agent.master;
  $("zainValue").textContent = agent.zain;
  $("phoneValue").textContent = agent.phone;
  $("qrValue").textContent = agent.qr;
  $("selectedAgentText").textContent = agent.name[currentLang];
}

document.querySelectorAll(".lang-btn").forEach((btn) => {
  btn.addEventListener("click", () => applyLanguage(btn.dataset.lang));
});

document.querySelectorAll(".amount-btn").forEach((btn) => {
  btn.addEventListener("click", () => {
    document.querySelectorAll(".amount-btn").forEach((b) => b.classList.remove("active"));
    btn.classList.add("active");
    selectedAmount = Number(btn.dataset.amount);
    $("customAmount").value = "";
    updateSummary();
  });
});

$("customAmount").addEventListener("input", () => {
  updateSummary();
});

document.querySelectorAll(".agent-card").forEach((card) => {
  card.addEventListener("click", () => {
    document.querySelectorAll(".agent-card").forEach((c) => c.classList.remove("active"));
    card.classList.add("active");
    selectedAgent = Number(card.dataset.agent);
    updateAgentInfo();
  });
});

document.querySelectorAll(".copy-btn").forEach((btn) => {
  btn.addEventListener("click", async () => {
    const targetId = btn.dataset.copyTarget;
    const text = $(targetId).textContent;
    const original = content[currentLang].copy;
    const copied = content[currentLang].copied;

    try {
      await navigator.clipboard.writeText(text);
      btn.textContent = copied;
      setTimeout(() => {
        btn.textContent = original;
      }, 1200);
    } catch (err) {
      alert(text);
    }
  });
});

$("confirmBtn").addEventListener("click", () => {
  $("reportSection").classList.remove("hidden");
  $("reportSection").scrollIntoView({ behavior: "smooth", block: "start" });
});

$("contactBtn").addEventListener("click", () => {
  const phone = agents[selectedAgent].phone.replace(/\s+/g, "");
  window.location.href = `tel:${phone}`;
});

$("reportForm").addEventListener("submit", (e) => {
  e.preventDefault();
  $("successBox").classList.remove("hidden");
  $("successBox").scrollIntoView({ behavior: "smooth", block: "nearest" });
});

applyLanguage("fa");
updateSummary();
updateAgentInfo();
