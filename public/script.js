$("contactBtn").addEventListener("click", () => {
  const phone = agents[selectedAgent].phone.replace(/\s+/g, "");
  window.location.href = `tel:${phone}`;
});

$("reportForm").addEventListener("submit", (e) => {
  e.preventDefault();

  const displayName = $("reportDedicationName").value.trim() || $("payerName").value.trim() || "New Supporter";

  const newReport = {
    name: displayName,
    country: $("payerCountry").value,
    amount: amountValue(),
    agent: selectedAgent,
    time: new Date().toISOString().slice(0, 16).replace("T", " "),
    note: $("paymentNote").value.trim() || i18n[currentLang].reportNoteFallback
  };

  sampleReports.unshift(newReport);
  sampleReports.splice(20);

  $("successBox").classList.remove("hidden");
  $("reportForm").reset();

  $("dedicationName").value = i18n[currentLang].dedicationNameDefault;
  $("reportDedicationName").value = i18n[currentLang].dedicationNameDefault;

  selectedAmount = 10;
  $("customAmount").value = "";
  document.querySelectorAll(".amount-btn").forEach((b) => b.classList.remove("active"));
  document.querySelector('.amount-btn[data-amount="10"]').classList.add("active");

  renderReports();
  updateSummary();

  $("successBox").scrollIntoView({ behavior: "smooth", block: "nearest" });
});

